  /** One node of the sidebar module tree: a single path segment that is either a package (has children), a documented
    * module (has a page), or — rarely — both (a module whose name also prefixes deeper modules).
    */
  private case class TreeNode(segment: String, fullPath: String, module: Option[DocModule], children: Seq[TreeNode])

  /** Groups the modules into a directory tree by their dotted path, packages (directories) before leaf modules, each
    * alphabetical. `active` is the title of the currently-rendered module, highlighted in its page's sidebar.
    */
  private def renderSidebar(modules: Seq[DocModule], active: Option[String]): String = {
    val tree = buildForest(modules.map(m => ((m.name.packages :+ m.name.name).toList, m)), "")
    s"""<nav class="sidebar">
       |<a class="brand" href="index.html">eliot<span class="eliot-cursor"></span><span class="tag">apidoc</span></a>
       |<input class="search" type="search" placeholder="Filter modules…" oninput="filterModules(this.value)" aria-label="Filter modules">
       |<ul class="tree" id="tree">
       |${renderForest(tree, active)}
       |</ul>
       |</nav>""".stripMargin
  }

  private def buildForest(entries: Seq[(List[String], DocModule)], prefix: String): Seq[TreeNode] =
    entries
      .groupBy(_._1.head)
      .toSeq
      .map { case (segment, group) =>
        val path   = if (prefix.isEmpty) segment else s"$prefix.$segment"
        val module = group.collectFirst { case (_ :: Nil, m) => m }
        val deeper = group.collect { case (_ :: rest, m) if rest.nonEmpty => (rest, m) }
        TreeNode(segment, path, module, buildForest(deeper, path))
      }
      .sortBy(node => (node.children.isEmpty, node.segment.toLowerCase))

  private def renderForest(nodes: Seq[TreeNode], active: Option[String]): String =
    nodes.map(renderTreeNode(_, active)).mkString("\n")

  private def renderTreeNode(node: TreeNode, active: Option[String]): String =
    if (node.children.isEmpty)
      node.module match {
        case Some(m) =>
          val cls = if (active.contains(m.title)) """ class="active"""" else ""
          s"""<li class="tree-leaf" data-name="${escape(m.title)}"><a$cls href="${escape(m.fileName)}">${escape(node.segment)}</a></li>"""
        case None    =>
          s"""<li class="tree-leaf" data-name="${escape(node.fullPath)}"><span>${escape(node.segment)}</span></li>"""
      }
    else {
      val summary = node.module match {
        case Some(m) => s"""<a href="${escape(m.fileName)}">${escape(node.segment)}</a>"""
        case None    => escape(node.segment)
      }
      s"""<li class="tree-dir" data-name="${escape(node.fullPath)}"><details class="tree-pkg" open><summary>$summary</summary><ul class="tree">${renderForest(node.children, active)}</ul></details></li>"""
    }

  private def indexContent(modules: Seq[DocModule]): String = {
    val totalItems = modules.map(_.items.size).sum
    val rows = modules
      .map { module =>
        val kinds = module.items.groupBy(_.kind).view.mapValues(_.size)
        val summary = DocItem.Kind.values.toSeq
          .flatMap(k => kinds.get(k).map(n => s"""<span class="count">$n ${escape(k.label)}</span>"""))
          .mkString
        s"""<li><a href="${escape(module.fileName)}">${escape(module.title)}</a>$summary</li>"""
      }
      .mkString("\n")
    s"""<h1>Eliot API documentation</h1>
       |<p class="lead">${modules.size} modules · $totalItems documented definitions. Layers are merged: each name shows its abstract signature together with the platforms that implement it.</p>
       |<ul class="moduleindex">
       |$rows
       |</ul>""".stripMargin
  }

  private def moduleContent(module: DocModule): String = {
    val doc      = module.doc.map(d => s"""<div class="doc">${MarkdownRenderer.render(d)}</div>""").getOrElse("")
    val sections = DocItem.Kind.values.toSeq.flatMap { kind =>
      val items = module.items.filter(_.kind == kind)
      if (items.isEmpty) None
      else Some(s"""<section class="group"><h2 class="group-title">${escape(sectionTitle(kind))}</h2>${items.map(renderItem).mkString}</section>""")
    }
    s"""<h1 class="module-title"><span class="chip chip-module">module</span>${escape(module.title)}</h1>$doc${sections.mkString}"""
  }

  private def sectionTitle(kind: DocItem.Kind): String = kind match {
    case DocItem.Kind.TypeLike => "Types"
    case DocItem.Kind.Ability  => "Abilities"
    case DocItem.Kind.Value    => "Definitions"
  }

  private def renderItem(item: DocItem): String = {
    val badges = badgesFor(item)
    val doc    = item.doc.map(d => s"""<div class="doc">${MarkdownRenderer.render(d)}</div>""").getOrElse("")
    val members =
      if (item.members.isEmpty) ""
      else {
        val label = if (item.kind == DocItem.Kind.Ability) "Methods" else "Definitions"
        val rows  = item.members.map(renderMember).mkString
        s"""<div class="members"><h3>$label</h3>$rows</div>"""
      }
    val implementations =
      if (item.implementations.isEmpty) ""
      else s"""<div class="members"><h3>Implementations</h3>${item.implementations.map(renderImplementation).mkString}</div>"""

    s"""<section class="item item-${item.kind.toString.toLowerCase}" id="${escape(item.anchor)}">
       |<div class="item-head"><span class="chip chip-${item.kind.toString.toLowerCase}">${escape(item.kind.label)}</span><h2 class="item-name"><a href="#${escape(item.anchor)}">${escape(item.name)}</a></h2>$badges</div>
       |<pre class="sig"><code>${EliotHighlighter.highlight(item.signature)}</code></pre>
       |$doc$members$implementations
       |</section>""".stripMargin
  }

  private def renderMember(member: DocItem.Member): String = {
    val layer = member.layer.map(l => s"""<span class="badge badge-layer">${escape(l)}</span>""").getOrElse("")
    val doc   = member.doc.map(d => s"""<div class="doc">${MarkdownRenderer.render(d)}</div>""").getOrElse("")
    s"""<div class="member"><div class="member-sig"><pre class="sig"><code>${EliotHighlighter.highlight(member.signature)}</code></pre>$layer</div>$doc</div>"""
  }

  private def renderImplementation(impl: DocItem.Implementation): String = {
    val layers = impl.layers.map(l => s"""<span class="badge badge-layer">${escape(l)}</span>""").mkString
    val doc    = impl.doc.map(d => s"""<div class="doc">${MarkdownRenderer.render(d)}</div>""").getOrElse("")
    s"""<div class="member"><div class="member-sig"><pre class="sig"><code>${EliotHighlighter.highlight(impl.signature)}</code></pre>$layers</div>$doc</div>"""
  }

  private def badgesFor(item: DocItem): String = {
    val implemented =
      if (item.implementedOn.nonEmpty)
        item.implementedOn.map(l => s"""<span class="badge badge-impl" title="implemented on $l">$l</span>""").mkString
      else if (item.kind != DocItem.Kind.Ability) """<span class="badge badge-abstract">abstract</span>"""
      else ""
    s"""<span class="badges">$implemented</span>"""
  }

