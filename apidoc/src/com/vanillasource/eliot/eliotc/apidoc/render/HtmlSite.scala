/** Renders the documentation model to a static HTML site styled with the Eliot brand design system: an `index.html`
  * landing page, one page per module, and a shared `style.css`. The look follows the brand's documentation kit — a
  * light long-form reading surface (`.on-paper`) with a sticky module sidebar, a single signal-green accent, and
  * JetBrains Mono / Inter typography. Code is the hero: every signature and example renders in a dark `ink` well with
  * green-keyword syntax highlighting. Each card shows the highlighted signature, layer/implementation badges, the
  * rendered Markdown documentation, and (for abilities and merged types) nested members and implementations.
  *
  * The stylesheet is the design system's token files (vendored verbatim under `design/`, see that folder's README)
  * followed by apidoc's own component rules, which reference only the semantic tokens — so brand changes flow in by
  * re-syncing the token files, with no Scala edits. The only external assets are the two webfonts (Fontsource CDN,
  * `font-display: swap` with system fallbacks); the site otherwise renders fully offline.
  *
  * Returns the site as `(relativePath, content)` pairs for the caller to write to disk.
  */
object HtmlSite {
  def render(modules: Seq[DocModule]): Seq[(String, String)] = {
    val indexPage =
      "index.html" -> page("Eliot API documentation", renderSidebar(modules, None), indexContent(modules))
    val modulePages =
      modules.map(module => module.fileName -> page(module.title, renderSidebar(modules, Some(module.title)), moduleContent(module)))
    (indexPage +: modulePages) :+ ("style.css" -> css)
  }

  private def escape(text: String): String = EliotHighlighter.escape(text)

  private def page(title: String, sidebar: String, content: String): String =
